# int
score = 100
print(score, type(score))

# string
c = 'C'
print(c, type(c))

# boolean
x = False
print(x,type(x))

# float
price = 90.99
print(price, type(price))

# list
l = [1, 2, 5, "Python", 16.50, True]
print(l, type(l))


# tuple
tup = (5,6,9,"V", False)
print(tup, type(tup))

# dictionary 
info = {
    "couse name": "Python",
    "course price" : 1600,
    "Months to Complete" : 6 
}

print(info, type(info))

# set
s = {1,2,3,3,4}
print(s, type(s))

