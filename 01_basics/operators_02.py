# Relational Operator -> <, >, <=, >=, ==, !=

a = 12
b = 14

print(a > b) # False
print(a >= b) # False
print(a < b)  # True
print(a <= b) # True
print(a == b) # False
print(a != b) # True





# Logical Operators -> and, or, not

num1 = 102
num2 = 46

print(num1>num2 and num1<num2)
print(num1==num2 or num1>num2)
print(not(num1==num2 or num1<num2))

